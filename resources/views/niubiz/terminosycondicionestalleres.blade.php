        				<p style='text-align:justify'><strong><span>1.Antecedentes:</span></strong></p>

							<p style='text-align:justify'><span>La Municipalidad de Ancón, a través de su Subgerencia de Servicios Sociales y el área de
Deportes y Cultura, organiza anualmente los Talleres de Verano y las Escuelas Municipales en
diversas disciplinas deportivas, educativas y culturales. Estas actividades incluyen disciplinas
como fútbol, básquetbol, robótica, matemática, dibujo y pintura, entre otras. La municipalidad
ha implementado una plataforma en línea para gestionar la inscripción y el pago de estas
actividades, facilitando el acceso de los ciudadanos. Actualmente, se están revisando y
actualizando las Políticas y Condiciones de Uso del Servicio de Pago en Línea, con el objetivo
de brindar mayor claridad a los usuarios y asegurar la transparencia en los procesos de
inscripción y pago.</span></p>

							<p style='text-align:justify'><strong><span>2. Análisis</span></strong></p>

							<p style='text-align:justify'><span>En esta revisión, se han identificado los siguientes puntos clave de las políticas y condiciones
de uso del servicio de pago en línea:
</span></p>
<ol>
    <li>Descripción General del Servicio
El servicio de pago en línea permite a personas naturales inscribirse en los talleres de
verano y las escuelas municipales mediante una plataforma digital. Al realizar el pago,
los usuarios autorizan la transferencia de fondos para formalizar su inscripción en el
taller o escuela seleccionado, garantizándoles acceso a la actividad en los horarios y
lugares establecidos.</li>
<li>Políticas de Entrega</li>
<ul>
    <li>
    El proceso de inscripción se confirma cuando el usuario efectúa el pago. El
monto es transferido a la entidad que proporciona el servicio, y el usuario
recibe la confirmación de que su transacción ha sido completada
    </li>
    <li>
    Una vez finalizado el proceso, la entidad prestadora garantiza que los usuarios
    recibirán el servicio en las condiciones previamente pactadas
    </li>
</ul>
</ol>

							<p style='text-align:justify'><strong><span>3. Anulaciones y Correcciones</span></strong></p>

							<ul>
                                <li>
                                Las transacciones de pago en línea son definitivas. Debido a la naturaleza de
                                estos pagos, no pueden ser anuladas ni modificadas una vez realizadas.
                                </li>
                                <li>
                                En caso de errores en los datos de pago, el usuario debe dirigirse a la entidad
                                proveedora para cualquier gestión posterior.
                                </li>
                            </ul>

